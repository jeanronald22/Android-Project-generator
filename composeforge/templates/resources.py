"""
composeforge.templates.resources
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
Android resource XML templates: strings, themes, icons, backup rules.
"""


def strings_xml(app_name: str) -> str:
    """Génère res/values/strings.xml."""
    return f"""\
<resources>
    <string name="app_name">{app_name}</string>
</resources>
"""


def themes_xml(safe_name: str) -> str:
    """Génère res/values/themes.xml (thème de base pour le splash/manifest)."""
    return f"""\
<?xml version="1.0" encoding="utf-8"?>
<resources>
    <style name="Theme.{safe_name}" parent="android:Theme.Material.Light.NoActionBar">
        <item name="android:statusBarColor">@android:color/transparent</item>
        <item name="android:navigationBarColor">@android:color/transparent</item>
    </style>
</resources>
"""


def themes_night_xml(safe_name: str) -> str:
    """Génère res/values-night/themes.xml."""
